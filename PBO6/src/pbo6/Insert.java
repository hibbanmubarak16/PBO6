    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
     if (txtIdbuku.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Isi Semua Data!");
        } else if (txtJudul.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Isi Semua Data!");
        } else if (txtGenre.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Isi Semua Data!");
        } else if (txtPengarang.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Isi Semua Data!");
        } else if (txtTahunterbit.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Isi Semua Data!");
        } else    {
            try {
                String sql = "INSERT INTO koleksi_buku (id_buku, judul, genre, pengarang, tahun_terbit) VALUES (?, ?, ?, ?, ?)";
                var ps = conn.prepareStatement(sql);
                ps.setString(1, txtIdbuku.getText());
                ps.setString(2, txtJudul.getText());
                ps.setString(3, txtGenre.getText());
                ps.setString(4, txtPengarang.getText());
                ps.setString(5, txtTahunterbit.getText());
                ps.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data berhasil disimpan!");
                bersih();
                dispose();

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            }
        }   // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

