    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    try {
                String sql = "DELETE FROM koleksi_buku " + "WHERE id_buku=? AND judul=? AND genre=? AND pengarang=? AND tahun_terbit=? ";

                var ps = conn.prepareStatement(sql);

                ps.setString(1, IdbukuLama);
                ps.setString(2, JudulLama);
                ps.setString(3, GenreLama);
                ps.setString(4, PengarangLama);
                ps.setString(5, TahunterbitLama);

                int rows = ps.executeUpdate();
                if (rows > 0) {
                    JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
                    dispose();
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Error: data tidak ditemukan " + e.getMessage());
            }   // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

